'''
5.编写程序，根据下列的函数关系，对输入的每个x值，计算出相应的y值。
x<0       y=0
0<x<=10   y=x
10<x<=20  y=10
20<x<40   y=-0.5*x+20
测试1：（第1行为输入，第2行为输出）
-8
0
测试2：（第1行为输入，第2行为输出）
5
5
测试3：（第1行为输入，第2行为输出）
24  
8.0
'''
x = int(input())
if x <0:
    y = 0
elif 0<x<=10:
    y = x
elif 10 <x<=20:
    y = 10
elif 20<x<40:
    y = -0.5*x +20
print(y)
